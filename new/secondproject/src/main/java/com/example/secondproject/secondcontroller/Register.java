package com.example.secondproject.secondcontroller;

public class Register{
	private Integer sno;
	private String sname;
	private String email;
	private String mobile;
	private Integer bcode;
	
	private String branch;
	private String hod;
	private String branchPh;
	
	
	public Integer getSno() {
		return sno;
	}
	public void setSno(Integer sno) {
		this.sno = sno;
	}
	public Integer getBcode() {
		return bcode;
	}
	public void setBcode(Integer bcode) {
		this.bcode = bcode;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getHod() {
		return hod;
	}
	public void setHod(String hod) {
		this.hod = hod;
	}
	public String getBranchPh() {
		return branchPh;
	}
	public void setBranchPh(String branchPh) {
		this.branchPh = branchPh;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

}
